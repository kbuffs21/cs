/* 
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int divpwr2(int x, int n) 
{
//set row of ones from n-bit rightward with mask: 0s if pos or 1s if neg creates bias then carry out divsion 
  int a = (((1 << n) + ~0) & (x >> 31));
  return (x + a) >> n;
}

/* 
 * isEqual - return 1 if x == y, and 0 otherwise 
 *   Examples: isEqual(5,5) = 1, isEqual(4,5) = 0
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int isEqual(int x, int y)
{
// x xor with y = 0 if x =y, !0 = 1  !not-zero = 0
  return !(x^y);
}

/* 
 * negate - return -x 
 *   Example: negate(1) = -1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int negate(int x) 
{
//compliment bits and add one for twos comp offset
  return (~x + 1);
}

/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) 
{
// shift wanted byte over to first position then mask from rest
  int a = x >> (n << 3);
  return (a & 0xff);
}

/* 
 * isPositive - return 1 if x > 0, return 0 otherwise 
 *   Example: isPositive(-1) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 3
 */
int isPositive(int x)
{
// 1st term: 0 = pos, 1 = neg - XOR - 2nd term: 0 = 0, 1 = non zero, no neg zeros in twos comp
  return (((x >> 31) & 0x1) ^ (!!x));
}

/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) 
{
//opposite 1st term: 1 if signs are same AND 2nd term: 1 if x and sum have opposite signs
  int a = x >> 31;
  int b = y >> 31;
  int c = ((x + y) >> 31);
  return  !(~(a ^ b) & (a ^ c));
}

/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) 
{
// opposite - 1st term: only 0 if y < x and x and y both pos or negXOR 2nd term: only 1 if x is neg and y is pos
  int a = x >> 31;
  int b = y >> 31;
  int c = (y + (~x + 1)) >> 31;
  return !((c | (a ^ b)) ^ (a & (a ^ b)));
}

/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 1 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3 
 */
int logicalShift(int x, int n) 
{// perform arithmetic shift, if negative then switch 1s to zeros with AND 1s mask
  return ((x >> n) & ((1 << (32 + (~n + 1))) + ~0));
}

/* 
 * leastBitPos - return a mask that marks the position of the
 *               least significant 1 bit. If x == 0, return 0
 *   Example: leastBitPos(96) = 0x20
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 4 
 */
int leastBitPos(int x) 
{
// lsb will be only bit if x is and with -x, if x =  0 => 0 & 0 = 0
  return (x & (~x + 1));
}

/*
 * bitParity - returns 1 if x contains an odd number of 0's
 *   Examples: bitParity(5) = 0, bitParity(7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int bitParity(int x) 
{
// x(n+1) will have same parity as x(n): 0 if even, 1 if odd then mask lsb
  x = x ^ (x >> 16);
  x = x ^ (x >> 8);
  x = x ^ (x >> 4);
  x = x ^ (x >> 2);
  x = x ^ (x >> 1);
  return (x & 1);
}

/*
 * isPower2 - returns 1 if x is a power of 2, and 0 otherwise
 *   Examples: isPower2(5) = 0, isPower2(8) = 1, isPower2(0) = 0
 *   Note that no negative number is a power of 2.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 60
 *   Rating: 4
 */
int isPower2(int x) 
{
//opposite (x & x - 1) = 0 if x is pwr2, zero and neg numbers cant be pwr2
  int a = x >> 31;
  return !((x & (x + ~0)) + !x + a);
}

/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) 
{
//will be all zeros if x is zere then add 1 to get 1, or all ones if x is not 0 then add 1 to get 0 
  return ((((~x + 1) | x) >> 31) + 1);
}
